x = 10
y = 3

print(x > 9)                # x가 9보다 큰가?
print(y <= 10)              # y가 10보다 작거나  같은가?
print(x + y == 13)          # x + y가  13과  같은가?
print(x % 2 == 0)           # x를 2로 나눈 나머지가 0인가? 즉, 짝수인가?
print(y % 2 == 0)           # y를 2로 나눈 나머지가 0인가? 즉, 짝수인가?
print(x % 4 == 0)           # x가 4의 배수인가?
print(y % 3 != 0)           # y는 3의 배수가 아닌가?